function f(val) {
    var ans="";
    switch (val) {
        case 1:
            case 2:
                case 3:
                    ans="low";
                    break;
                    case 5:
                        case 6:
                            ans="mid";
                            break;
        case 7:
        case 8:
            ans="high";
            break;


    }
return ans;
}
console.log(f(3));